#include "h.h"

void	ft_iso(t_ptr *ptr)
{
	ptr->iso[0][0] = 1200 / ptr->t[X];
	ptr->iso[0][1] = -1;
	ptr->iso[1][0] = 2;
	ptr->iso[1][1] = 1000 / ptr->t[Y];
	ptr->iso[2][0] = 2;
	ptr->iso[2][1] = 1;
	ptr->o[X] = WIDTH / 2 - ptr->t[X] * ptr->iso[0][0];
	ptr->o[Y] = (HEIGHT - ptr->t[Y] * ptr->iso[1][1]) / 2;
}
